/// Semantic spacing values for consistent UI spacing throughout the app
abstract class Spacing {
  /// Extra small spacing: 4.0
  static const double xs = 4.0;

  /// Small spacing: 8.0
  static const double s = 8.0;

  /// Small-medium spacing: 12.0
  static const double sm = 12.0;

  /// Medium spacing: 16.0
  static const double m = 16.0;

  /// Medium-large spacing: 24.0
  static const double ml = 24.0;

  /// Large spacing: 32.0
  static const double l = 32.0;

  /// Large-extra spacing: 48.0
  static const double lx = 48.0;

  /// Extra large spacing: 64.0
  static const double xl = 64.0;
}
